static void set_pixel(uint8_t *buf, int x, int y, uint32_t color) {
    if (x < 0 || x >= VIDEO_WIDTH || y < 0 || y >= VIDEO_HEIGHT) return;
    ((uint32_t*)buf)[y * VIDEO_WIDTH + x] = color;
}

static void draw_char(uint8_t *buf, int x, int y, char c, uint32_t color) {
    if (c < 32 || c > 126) return;
    const uint8_t *bitmap = font_8x8[c - 32];
    for (int dy = 0; dy < 8; dy++) {
        for (int dx = 0; dx < 8; dx++) {
            if (bitmap[dy] & (1 << (7 - dx))) {
                set_pixel(buf, x + dx, y + dy, color);
            }
        }
    }
}

static void draw_text(uint8_t *buf, int x, int y, const char *text, uint32_t color) {
    int start_x = x;
    while (*text) {
        if (*text == '\n') {
            y += 10;
            x = start_x;
        } else {
            draw_char(buf, x, y, *text, color);
            x += 8;
        }
        text++;
    }
}

/**
 * Render the current framebuffer to the video callback.
 *
 * retro_display_messagesに設定されたメッセージを表示する。
 */
static void render_framebuffer(void)
{
    // Clear framebuffer to black
    memset(frame_buf, 0, VIDEO_PIXELS * sizeof(uint32_t));

    // Draw message if any
    if (retro_display_messages[0]) {
        draw_text(frame_buf, 10, 10, retro_display_messages, 0xFFFFFFFF); // White text
    }

	video_cb(frame_buf, VIDEO_WIDTH, VIDEO_HEIGHT, VIDEO_WIDTH * sizeof(uint32_t));
}

static void fallback_log(enum retro_log_level level, const char *fmt, ...)
{
   (void)level;
   va_list va;
   va_start(va, fmt);
   vfprintf(stderr, fmt, va);
   va_end(va);
}

void retro_init(void)
{
   frame_buf = (uint8_t*)malloc(VIDEO_PIXELS * sizeof(uint32_t));
   const char *dir = NULL;
   if (environ_cb(RETRO_ENVIRONMENT_GET_SYSTEM_DIRECTORY, &dir) && dir)
   {
      snprintf(retro_base_directory, sizeof(retro_base_directory), "SYSTEM_DIRECTORY: %s", dir);
   }

   init_core_options();
}

void retro_deinit(void)
{
   free(frame_buf);
   frame_buf = NULL;
}

